class Data:
    lines = {}
    name = b""
    summ = 0

    def calc_summ(self):
        summ = 0
        addr = 0x2201
        for i in self.lines:
            addr += len(self.lines[i]) + 5
            line = addr.to_bytes(2, "little") + \
                i.to_bytes(2, "little") + self.lines[i]
            for j in line:
                summ += j
        self.summ = summ & 0xffff


